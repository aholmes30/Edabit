package veryEasy;

public class IsTheNumberLessThanOrEqualToZero {

    public static boolean lessThanOrEqualToZero(int num) {

        return num <= 0;

    }

    public static void main(String[] args) {

        System.out.println(lessThanOrEqualToZero(7));
    }

}




